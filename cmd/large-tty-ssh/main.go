package main

import (
	"context"
	"log"
	"os"
	"os/signal"
	"syscall"

	"github.com/mamuzad/large-tty/internal/server"
)

func main() {
	ctx, stop := signal.NotifyContext(
		context.Background(),
		os.Interrupt, syscall.SIGTERM,
	)
	defer stop()

	if err := server.Start(ctx); err != nil {
		log.Fatal(err)
	}
}

